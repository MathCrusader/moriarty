std::vector<TestCase> GenerateBuilder::Run() const {
  if (generators_.empty()) {
    throw ConfigurationError("Generate::Run",
                             "No generators specified. Call `Using()` to add "
                             "generators.");
  }

  std::vector<GeneratedCase> all_test_cases;
  std::span<const std::string> input_deps;
  if (problem_.GetInputDependencies()) {
    const auto& deps = *problem_.GetInputDependencies();
    input_deps = {deps.data(), deps.size()};
  }

  for (const auto& [name, generator, options] : generators_) {
    std::vector<int64_t> seed =
        problem_.BaseSeedForGenerator(options.seed.value_or(name));
    seed.push_back(0);  // Placeholder for call number

    if (std::holds_alternative<NamedGenerator::ToTestCase>(generator)) {
      const auto& gen = std::get<NamedGenerator::ToTestCase>(generator);
      std::vector<GeneratedCase> cases =
          GenerateTestCaseGenerator(name, options.num_calls, seed, options.args,
                                    problem_.UnsafeGetVariables(), gen);
      all_test_cases.insert(all_test_cases.end(),
                            std::make_move_iterator(cases.begin()),
                            std::make_move_iterator(cases.end()));
    } else {
      const auto& gen = std::get<NamedGenerator::ToMTestCase>(generator);
      std::vector<GeneratedCase> cases = GenerateMTestCaseGenerator(
          name, options.num_calls, seed, options.args, input_deps,
          problem_.UnsafeGetVariables(), gen);
      all_test_cases.insert(all_test_cases.end(),
                            std::make_move_iterator(cases.begin()),
                            std::make_move_iterator(cases.end()));
    }
  }

  // TODO: Validate after the file split occurs once we allow multiple test
  // cases per file.
  ValidateTestCases<GeneratedCase>(
      problem_, all_test_cases,
      [](const GeneratedCase& item) -> const TestCase& {
        return item.test_case;
      });

  auto include_output = [&](const GeneratedCase& test_case) {
    if (!problem_.GetOutputWriter() || !problem_.GetOutputDependencies())
      return false;

    const auto& output_deps = *problem_.GetOutputDependencies();
    // FIXME: This is technically not true, since the output may be printing a
    // static output, but that's low priority to support.
    if (output_deps.empty()) return false;
    for (const auto& var_name : output_deps) {
      if (!test_case.test_case.UnsafeGetValues().Contains(var_name)) {
        return false;
      }
    }
    return true;
  };

  if (write_options_) {
    for (const auto& test_case : all_test_cases) {
      PrintTestCase(*write_options_, test_case, problem_.UnsafeGetVariables(),
                    problem_.GetInputWriter(),
                    include_output(test_case) ? problem_.GetOutputWriter()
                                              : std::nullopt);
    }
  }

  // TODO: Consider returning the metadata as well.
  std::vector<TestCase> test_cases;
  test_cases.reserve(all_test_cases.size());
  for (auto& generated_case : all_test_cases)
    test_cases.push_back(std::move(generated_case.test_case));

  return test_cases;
}

