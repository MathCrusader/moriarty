std::vector<GeneratedCase> GenerateMTestCaseGenerator(
    std::string_view generator_name, int num_calls,
    std::span<int64_t> seed_with_placeholder,
    const std::unordered_map<std::string, std::string>& args,
    std::span<const std::string> input_deps,
    const moriarty_internal::VariableSet& variables,
    const std::function<std::vector<MTestCase>(GenerateContext)>& generator) {
  std::vector<GeneratedCase> test_cases;
  for (int call = 0; call < num_calls; call++) {
    seed_with_placeholder.back() = call + 1;

    moriarty_internal::ValueSet values;
    moriarty_internal::RandomEngine rng(seed_with_placeholder, "v0.1");
    GenerateContext ctx(variables, values, rng, args);

    std::vector<MTestCase> generated_cases = generator(ctx);
    for (int idx = 1; const MTestCase& test_case : generated_cases) {
      test_cases.push_back(
          {.generator_name = std::string(generator_name),
           .generator_call_num = call,
           .index = idx++,
           .test_case = TestCase(moriarty_internal::GenerateAllValues(
               variables, test_case.UnsafeGetVariables(),
               test_case.UnsafeGetValues(),
               {
                   .random_engine = rng,
                   .variables_to_generate = input_deps,
               }))});
    }
  }
  return test_cases;
}

std::vector<GeneratedCase> GenerateTestCaseGenerator(
    std::string_view generator_name, int num_calls,
    std::span<int64_t> seed_with_placeholder,
    const std::unordered_map<std::string, std::string>& args,
    const moriarty_internal::VariableSet& variables,
    const std::function<std::vector<TestCase>(GenerateContext)>& generator) {
  std::vector<GeneratedCase> test_cases;
  for (int call = 0; call < num_calls; call++) {
    seed_with_placeholder.back() = call + 1;

    moriarty_internal::ValueSet values;
    moriarty_internal::RandomEngine rng(seed_with_placeholder, "v0.1");
    GenerateContext ctx(variables, values, rng, args);

    std::vector<TestCase> generated_cases = generator(ctx);
    for (int idx = 1; TestCase& test_case : generated_cases) {
      test_cases.push_back({.generator_name = std::string(generator_name),
                            .generator_call_num = call,
                            .index = idx++,
                            .test_case = std::move(test_case)});
    }
  }
  return test_cases;
}

std::filesystem::path FormatFilename(std::string pattern,
                                     const GeneratedCase& test_case,
                                     std::string_view suffix) {
  auto replace = [&](std::string_view from, std::string_view to) {
    for (size_t pos = pattern.find(from); pos != std::string_view::npos;
         pos = pattern.find(from, pos + to.size())) {
      pattern.replace(pos, from.size(), to);
    }
  };

  replace("{gen}", test_case.generator_name);
  replace("{call}", std::to_string(test_case.generator_call_num));
  replace("{idx}", std::to_string(test_case.index));
  pattern += suffix;
  return pattern;
};

void PrintTestCase(const GenerateBuilder::WriteOptions& options,
                   const GeneratedCase& test_case,
                   const moriarty_internal::VariableSet& variables,
                   const std::optional<WriterFn>& input_writer,
                   const std::optional<WriterFn>& output_writer) {
  if (!input_writer) {
    throw ConfigurationError(
        "Generate::Run",
        "No InputFormat specified in Problem. Cannot write input.");
  }

  std::unique_ptr<std::ofstream> owned_stream;
  auto get_ostream = [&](std::string_view ext) -> std::ostream* {
    if (std::holds_alternative<GenerateBuilder::FilenamePattern>(options)) {
      const auto& pattern = std::get<GenerateBuilder::FilenamePattern>(options);
      namespace fs = std::filesystem;
      fs::path filename = FormatFilename(pattern, test_case, ext);
      fs::path parent_path = filename.parent_path();
      if (!parent_path.empty()) {
        std::error_code ec;
        fs::create_directories(parent_path, ec);
        if (ec) {
          throw ConfigurationError(
              "Generate::Run",
              std::format("Failed to create directories for '{}': {}.",
                          filename.string(), ec.message()));
        }
      }

      owned_stream = std::make_unique<std::ofstream>(filename);
      if (!owned_stream->is_open()) {
        throw ConfigurationError(
            "Generate::Run",
            std::format("Failed to open file '{}' for writing.",
                        filename.string()));
      }
      return owned_stream.get();
    } else {
      return &std::get<WriteStreams>(options).input.get();
    }
  };

  {
    std::ostream* input_os = get_ostream(".in");
    moriarty_internal::ValueSet values;
    WriteContext ctx(*input_os, variables,
                     test_case.test_case.UnsafeGetValues());
    std::invoke(*input_writer, ctx,
                std::span<const TestCase>(&test_case.test_case, 1));
  }

  if (output_writer) {
    std::ostream* output_os = get_ostream(".ans");
    moriarty_internal::ValueSet values;
    WriteContext ctx(*output_os, variables,
                     test_case.test_case.UnsafeGetValues());
    std::invoke(*output_writer, ctx,
                std::span<const TestCase>(&test_case.test_case, 1));
  }
}

