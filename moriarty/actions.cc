std::string FailuresToString(
    const std::vector<DetailedValidationResult>& failures) {
  std::string result;

  for (const auto& [var_name, reason] : failures) {
    if (!result.empty()) result += "\n";
    result += reason.PrettyReason();
  }
  return result;
}

std::vector<TestCase> ReadTestCases(const ReaderFn& reader, ReadContext ctx) {
  std::vector<TestCase> test_cases = reader(ctx);
  ctx.ReadEof();

  return test_cases;
}

struct GeneratedCase {
  // The name of the generator to create this test case.
  std::string generator_name;
  // Which iteration of the calls to the generator this is from.
  int generator_call_num;
  // Which test case this is among the test cases generated by this call.
  int index;

  TestCase test_case;
};

template <typename T>
void ValidateTestCases(
    const Problem& problem, const std::vector<T>& test_cases,
    std::function<const TestCase&(const T&)> get_test_case =
        [](const T& item) -> const TestCase& { return item; }) {
  if (test_cases.empty()) {
    throw ValidationError("ValidateTestCases", "No Test Cases.");
  }

  for (int case_num = 1; const auto& item : test_cases) {
    std::vector<DetailedValidationResult> failures =
        moriarty_internal::ValidateValues(
            problem.UnsafeGetVariables(), get_test_case(item).UnsafeGetValues(),
            {}, ValidationStyle::kOnlySetVariables);
    if (!failures.empty()) {
      if (test_cases.size() == 1) {
        throw ValidationError(FailuresToString(failures));
      }
      throw ValidationError(std::format("Case #{} invalid:\n{}", case_num,
                                        FailuresToString(failures)));
    }
    case_num++;
  }
}

